class SpatialNode(BaseModel):
    """
    Represents a node within the physical directed spatial graph.
    
    Attributes:
        zone_id: The unique identifier for this physical zone.
        capacity: Maximum safe density threshold (people/m^2).
        current_density: Current estimated density, updated continuously.
    """
    zone_id: str
    capacity: float  # Max safe capacity
    current_density: float = 0.0


class SpatialEdge(BaseModel):
    """
    Represents a directed egress route/corridor between two physical nodes.
    
    Attributes:
        source: Starting zone identifier.
        target: Destination zone identifier.
        max_flow_rate: Maximum capacity throughput in people/minute.
        current_flow_rate: Current rate of crowd flow, updated from telemetry.
    """
    source: str
    target: str
    max_flow_rate: float  # Max throughput in people/minute
    current_flow_rate: float = 0.0


class SpatialGraph:
    """
    Represents physical stadium corridors as a directed spatial graph matrix G = (V, E).
    Used to dynamically calculate alternative routing paths during zone overcrowding.
    """
    def __init__(self, nodes: List[SpatialNode], edges: List[SpatialEdge]):
        """
        Initializes the spatial graph with vertices (nodes) and edges.
        
        Args:
            nodes: List of SpatialNode objects representing venue zones.
            edges: List of SpatialEdge objects representing transit routes between zones.
        """
        # Node lookup map for O(1) query access
        self.nodes: Dict[str, SpatialNode] = {n.zone_id: n for n in nodes}
        
        # Adjacency matrix: maps source zone to a dictionary of target edges
        self.adjacency: Dict[str, Dict[str, SpatialEdge]] = {n.zone_id: {} for n in nodes}
        for edge in edges:
            if edge.source in self.adjacency and edge.target in self.adjacency:
                self.adjacency[edge.source][edge.target] = edge

    @staticmethod
    def get_default_world_cup_graph() -> "SpatialGraph":
        """
        Factory method to generate the default spatial graph representing FIFA 2026 stadium corridors.
        """
        return SpatialGraph(
            nodes=[
                SpatialNode(zone_id="Gate_A", capacity=2.5),
                SpatialNode(zone_id="Gate_B", capacity=2.5),
                SpatialNode(zone_id="Gate_C", capacity=2.0),
                SpatialNode(zone_id="Corridor_1", capacity=4.0),
                SpatialNode(zone_id="Corridor_2", capacity=4.0),
                SpatialNode(zone_id="Main_Concourse", capacity=5.0),
                SpatialNode(zone_id="Transit_Hub", capacity=6.0),
                SpatialNode(zone_id="Transit_Shuttle", capacity=5.0)
            ],
            edges=[
                SpatialEdge(source="Gate_A", target="Corridor_1", max_flow_rate=120.0),
                SpatialEdge(source="Gate_B", target="Corridor_2", max_flow_rate=120.0),
                SpatialEdge(source="Gate_C", target="Main_Concourse", max_flow_rate=80.0),
                SpatialEdge(source="Corridor_1", target="Main_Concourse", max_flow_rate=200.0),
                SpatialEdge(source="Corridor_2", target="Main_Concourse", max_flow_rate=200.0),
                SpatialEdge(source="Main_Concourse", target="Transit_Hub", max_flow_rate=300.0),
                SpatialEdge(source="Main_Concourse", target="Transit_Shuttle", max_flow_rate=250.0)
            ]
        )

    def update_node_density(self, zone_id: str, density: float) -> None:
        """
        Updates the current crowd density of a specific node.
        
        Args:
            zone_id: Node key identifier.
            density: The new measured density value.
        """
        if zone_id in self.nodes:
            self.nodes[zone_id].current_density = density

    def update_edge_flow(self, source: str, target: str, flow_rate: float) -> None:
        """
        Updates the current flow rate along a directed route between two nodes.
        
        Args:
            source: Source zone identifier.
            target: Target zone identifier.
            flow_rate: The updated flow rate in people/minute.
        """
        if source in self.adjacency and target in self.adjacency[source]:
            self.adjacency[source][target].current_flow_rate = flow_rate

    def get_alternative_route(self, overloaded_zone: str) -> Optional[str]:
        """
        Finds adjacent zones with density below capacity limits to reroute flow.
        Prefers the Cython-optimised path; falls back to pure Python on error.
        
        Args:
            overloaded_zone: The key identifier of the currently crowded zone.
            
        Returns:
            The zone ID of the adjacent neighbor with the most spare capacity,
            or None if no neighbor has available capacity.
        """
        # 1. Attempt to run Cython-optimized routing routine if loaded
        if HAS_CYTHON:
            try:
                return get_alternative_route_cy(self.nodes, self.adjacency, overloaded_zone)
            except Exception as exc:
                logger.warning("Cython route lookup failed, falling back to pure Python: %s", exc)

        # 2. Pure Python fallback routing logic
        if overloaded_zone not in self.adjacency:
            return None
        
        # Find all outgoing routes from the overloaded zone
        candidates = []
        for target, edge in self.adjacency[overloaded_zone].items():
            target_node = self.nodes.get(target)
            # Filter adjacent nodes that have spare capacity (current_density < capacity)
            if target_node and target_node.current_density < target_node.capacity:
                spare_capacity = target_node.capacity - target_node.current_density
                candidates.append((target, spare_capacity))
        
        # Select the candidate node with the maximum spare capacity in O(N) time complexity
        if candidates:
            return max(candidates, key=lambda x: x[1])[0]
            
        return None

    def find_optimal_egress_path(self, source_zone: str, target_zones: List[str]) -> Optional[List[str]]:
        """
        Calculates a multi-hop egress route from a source zone to a set of target exit zones.
        Prefers the compiled Cython-accelerated algorithm, falling back to a pure Python BFS.
        
        Args:
            source_zone: The key identifier of the starting node.
            target_zones: A list of candidate destination exit zone IDs.
            
        Returns:
            A list of zone IDs representing the path, or None if no path with spare capacity exists.
        """
        if HAS_CYTHON:
            try:
                return find_optimal_path_cy(self.nodes, self.adjacency, source_zone, target_zones)
            except Exception as exc:
                logger.warning("Cython optimal path search failed: %s", exc)
                
        # Pure Python fallback
        if source_zone is None or target_zones is None:
            return None
        if source_zone in target_zones:
            return [source_zone]
            
        queue = [[source_zone]]
        visited = {source_zone}
        while queue:
            path = queue.pop(0)
            current_zone = path[len(path) - 1]
            if current_zone in target_zones:
                return path
            if current_zone in self.adjacency:
                for neighbor in self.adjacency[current_zone]:
                    if neighbor not in visited:
                        node_obj = self.nodes.get(neighbor)
                        if node_obj and node_obj.current_density < node_obj.capacity:
                            visited.add(neighbor)
                            new_path = list(path)
                            new_path.append(neighbor)
                            queue.append(new_path)
        return None


