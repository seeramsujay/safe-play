# InterventionScript Definition
# ---------------------------------------------------------------------------
# Pre-defined valid hazard levels, gate control actions, and transit actions to enforce at runtime.
_VALID_HAZARD_LEVELS = {"low", "medium", "high", "critical"}
_VALID_GATE_ACTIONS = {"KEEP_OPEN", "SLOW_ENTRY", "CLOSE_IMMEDIATELY", "REVERSE_FLOW"}
_VALID_TRANSIT_ACTIONS = {"KEEP_NOMINAL", "INCREASE_SHUTTLE_FREQUENCY", "REDIRECT_METRO_PASSENGER_ENTRY", "HOLD_METRO_TRAINS"}

class InterventionScript(BaseModel):
    """
    Pydantic schema representing safety intervention scripts generated by 
    the Small Language Model (SLM) or static fallback rules for FIFA World Cup 2026.
    
    Attributes:
        zone_id: The physical zone ID this script applies to.
        hazard_level: Assessed level ('low', 'medium', 'high', or 'critical').
        action_required: Boolean flag indicating if operator confirmation is required.
        reroute_target: Optional zone ID to redirect traffic towards.
        signage_instruction_en: English text to display on dynamic digital signage.
        signage_instruction_es: Spanish text to display on dynamic digital signage.
        signage_instruction_fr: French text to display on dynamic digital signage.
        audio_announcement_en: English PA audio announcement transcript.
        audio_announcement_es: Spanish PA audio announcement transcript.
        audio_announcement_fr: French PA audio announcement transcript.
        gate_action: Turnstile actuation action ('KEEP_OPEN', 'SLOW_ENTRY', etc.).
        accessibility_route_target: Optional alternative zone ID for disabled/ADA visitors.
        accessibility_instruction: Optional specialized routing guidelines for ADA visitors.
        transit_dispatch_action: Subway/shuttle dispatch control action.
        transit_instruction: Public transit guidelines for coordinators and volunteers.
        rationale: Brief natural language explanation for the recommendation.
    """
    model_config = ConfigDict(extra="forbid")
    
    zone_id: str = Field(
        ..., 
        min_length=1, 
        max_length=64, 
        description="The physical stadium quadrant/vomitory identifier"
    )
    hazard_level: str = Field(
        ..., 
        description="Assessed hazard level: 'low', 'medium', 'high', 'critical'"
    )
    action_required: bool = Field(
        ..., 
        description="True if operator must approve signage or gate changes"
    )
    reroute_target: Optional[str] = Field(
        None, 
        description="Alternative zone_id to redirect crowd flow if action_required is True"
    )
    signage_instruction_en: str = Field(
        ..., 
        max_length=120, 
        description="English signage message (maximum 120 characters)"
    )
    signage_instruction_es: str = Field(
        ..., 
        max_length=120, 
        description="Spanish signage message translation (maximum 120 characters)"
    )
    signage_instruction_fr: str = Field(
        ..., 
        max_length=120, 
        description="French signage message translation (maximum 120 characters)"
    )
    audio_announcement_en: str = Field(
        ..., 
        max_length=200, 
        description="English PA audio transcript (maximum 200 characters)"
    )
    audio_announcement_es: str = Field(
        ..., 
        max_length=200, 
        description="Spanish PA audio transcript translation (maximum 200 characters)"
    )
    audio_announcement_fr: str = Field(
        ..., 
        max_length=200, 
        description="French PA audio transcript translation (maximum 200 characters)"
    )
    gate_action: str = Field(
        ..., 
        description="Turnstile/gate control action: 'KEEP_OPEN', 'SLOW_ENTRY', 'CLOSE_IMMEDIATELY', 'REVERSE_FLOW'"
    )
    accessibility_route_target: Optional[str] = Field(
        None, 
        description="Alternative ADA accessible route target"
    )
    accessibility_instruction: Optional[str] = Field(
        None, 
        description="Special ADA direction and guidance"
    )
    transit_dispatch_action: str = Field(
        ..., 
        description="Transit coordination command"
    )
    transit_instruction: str = Field(
        ..., 
        description="Transit coordination guidance message"
    )
    rationale: str = Field(
        ..., 
        max_length=120, 
        description="Zero-fluff explanation of the assessment (maximum 10 words)"
    )

    @field_validator("hazard_level")
    @classmethod
    def validate_hazard_level(cls, v: str) -> str:
        """Enforces that the hazard level matches one of the pre-approved severity tokens."""
        if v not in _VALID_HAZARD_LEVELS:
            raise ValueError(f"hazard_level must be one of {_VALID_HAZARD_LEVELS}")
        return v

    @field_validator("gate_action")
    @classmethod
    def validate_gate_action(cls, v: str) -> str:
        """Enforces that the gate action matches one of the pre-approved turnstile command states."""
        if v not in _VALID_GATE_ACTIONS:
            raise ValueError(f"gate_action must be one of {_VALID_GATE_ACTIONS}")
        return v

    @field_validator("transit_dispatch_action")
    @classmethod
    def validate_transit_dispatch_action(cls, v: str) -> str:
        """Enforces that the transit dispatch action matches one of the pre-approved transit command states."""
        if v not in _VALID_TRANSIT_ACTIONS:
            raise ValueError(f"transit_dispatch_action must be one of {_VALID_TRANSIT_ACTIONS}")
        return v


